# 你来制作一款游戏
# 函数的风格
# 1.类里面的函数称作"方法(method)"
# 2.在使用类的过程中,很大一部分时间都在告诉你的类如何"做事情"给函数名时,命名为一个动词,作为给类的一个命令
# 3.让函数保持简单小巧
# 类的风格
# 1.使用驼峰式大小写(camel case)
# 2.__init__不应该做太多事情,这会让类变得难以使用
# 3.因为其他函数应该使用下划线分歌词
# 4.用一致的方式组织函数的参数,如参数顺序应该一致
# 5.不要使用来模块的变量或全局变量
# 6.不要为了维持风格一致性而死板
# 代码风格
# 1.在代码字符之间留下一些空白
# 2.代码易读
# 3.学着模范别人的风格
# 好的注释
# 1. 注释很重要,在合作之中
# 2.描述清楚为什么要这样做
# 3.为函数编写文档注释,写写这个函数的用法
# 4.注释短小精悍,一语中的
